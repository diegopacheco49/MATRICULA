    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        try {
            
            
            
            String sql="select * from Usuarios where ID_usuario=? and CONTRASENIA=?";
            PreparedStatement pst=cn.prepareStatement(sql);
            pst.setString(1,txtusuario.getText());
            pst.setString(2,txtcontraseña.getText());
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"Bienvenido");
                Interfaz m=new Interfaz();
                m.setVisible(true);
                setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(this,"Usted a ingresado mal el usuario o la contraseña");
                txtusuario.setText("");
                txtcontraseña.setText("");
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_ingresarActionPerformed

